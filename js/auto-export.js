function getLanguage() {
  return window.TK168I18N?.getLanguage?.() || 'ja';
}

function copy(key, language = getLanguage()) {
  return EXPORT_COPY[language]?.[key] || EXPORT_COPY.ja[key] || EXPORT_COPY.zh[key] || key;
}

function applyExportCopy(language = getLanguage()) {
  document.title = copy('meta.title', language);
  document.querySelectorAll('[data-export-key]').forEach((element) => {
    const key = element.dataset.exportKey || '';
    const text = copy(key, language);
    if (text) element.textContent = text;
  });
}

function wireRevealAnimations() {
  const targets = document.querySelectorAll(
    '.export-card, .export-focus-card, .export-process-card, .export-coverage-card, .export-note-card, .export-inline-note'
  );

  if (!targets.length) return;

  targets.forEach((target) => target.classList.add('export-reveal'));

  if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
    targets.forEach((target) => target.classList.add('is-inview'));
    return;
  }

  const observer = new IntersectionObserver((entries, obs) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add('is-inview');
      obs.unobserve(entry.target);
    });
  }, {
    threshold: 0.12,
    rootMargin: '0px 0px -6% 0px'
  });

  targets.forEach((target) => observer.observe(target));
}

function initAutoExportPage() {
  window.TK168CommonLinks?.applyCommonLinks();
  window.TK168PageChrome?.applyPageChrome({
    pageKey: 'export',
    landingHref: 'index.html',
    aboutHref: 'about.html',
    inventoryHref: 'brand.html',
    serviceHref: 'service.html',
    rentalHref: 'rental.html',
    exportHref: 'auto-export.html'
  });

  applyExportCopy();
  wireRevealAnimations();
}

window.addEventListener('tk168:languagechange', () => {
  applyExportCopy();
});

initAutoExportPage();
