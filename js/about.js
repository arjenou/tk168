function applyAboutCopy(language = 'ja') {
  const catalog = ABOUT_COPY[language] || ABOUT_COPY.ja || ABOUT_COPY.zh;
  document.title = `${catalog.heroTitle} — TK168 Premium Automotive`;

  document.querySelectorAll('[data-about-key]').forEach((element) => {
    const key = element.dataset.aboutKey || '';
    if (Object.prototype.hasOwnProperty.call(catalog, key)) {
      element.textContent = catalog[key];
    }
  });
}

window.TK168CommonLinks?.applyCommonLinks();
window.TK168PageChrome?.applyPageChrome({
  pageKey: 'about',
  inventoryHref: 'brand.html',
  scrollThreshold: 48
});

applyAboutCopy(window.TK168I18N?.getLanguage?.() || 'ja');

window.addEventListener('tk168:languagechange', (event) => {
  applyAboutCopy(event.detail?.language || 'ja');
});

function initAboutNetworkSelection() {
  const cards = Array.from(document.querySelectorAll('.about-network-card'));
  if (!cards.length) return;

  const activate = (target) => {
    cards.forEach((card) => {
      const selected = card === target;
      card.classList.toggle('is-active', selected);
      card.setAttribute('aria-selected', selected ? 'true' : 'false');
    });
  };

  cards.forEach((card) => {
    card.setAttribute('role', 'button');
    card.setAttribute('tabindex', '0');
    card.setAttribute('aria-selected', 'false');

    card.addEventListener('click', () => activate(card));
    card.addEventListener('keydown', (event) => {
      if (event.key === 'Enter' || event.key === ' ') {
        event.preventDefault();
        activate(card);
      }
    });

  });
}

initAboutNetworkSelection();
