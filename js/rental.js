function escapeHtml(value) {
  return String(value ?? '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}

function formatMileage(value, language, mileageUnit) {
  const lang = language ?? (window.TK168I18N?.getLanguage?.() || 'ja');
  const formatted = window.TK168_DATA?.formatVehicleMileageDisplay?.(value, lang, mileageUnit);
  if (formatted) return formatted;
  const raw = String(value ?? '').trim();
  if (raw && !/\d/.test(raw)) return raw;
  return raw || '—';
}

function formatRate(value, language) {
  const display = getRentalDailyDisplayPrice(value, language) || '-';
  if (language === 'en') return `${display}/day`;
  return language === 'ja' ? `${display}/日` : `${display}/天`;
}

function formatMinDays(days, language) {
  if (language === 'en') return `${days} day${Number(days) === 1 ? '' : 's'}`;
  return language === 'ja' ? `${days}日` : `${days}天`;
}

function isRentalFleetMobileView() {
  return rentalFleetMobileViewport.matches;
}

function ensureRentalFleetVisibleLimit(list) {
  const n = list.length;
  if (!n) {
    rentalFleetVisibleLimit = 0;
    return;
  }
  if (n <= RENTAL_FLEET_BATCH) {
    rentalFleetVisibleLimit = n;
    return;
  }
  if (rentalFleetVisibleLimit <= 0) {
    rentalFleetVisibleLimit = RENTAL_FLEET_BATCH;
    return;
  }
  rentalFleetVisibleLimit = Math.min(rentalFleetVisibleLimit, n);
}

function getRentalFleetLoadMoreLabel() {
  const lang = getLanguage();
  return window.TK168I18N?.t('brand.loadMore')
    || window.TK168I18N?.t('home.loadMore')
    || (lang === 'zh' ? '加载更多' : lang === 'en' ? 'Load more' : 'もっと見る');
}

function updateRentalFleetLoadMoreUi(list) {
  const wrap = document.getElementById('rentalFleetLoadMoreWrap');
  const btn = document.getElementById('rentalFleetLoadMore');
  if (!wrap || !btn) return;
  const canLoadMore = list.length > rentalFleetVisibleLimit;
  wrap.hidden = !list.length || !canLoadMore;
  const label = getRentalFleetLoadMoreLabel();
  btn.textContent = label;
  btn.setAttribute('aria-label', label);
}

function appendRentableFleetCards(language, fromIdx, toIdx, list) {
  const grid = document.getElementById('rentalVehicleGrid');
  if (!grid || fromIdx >= toIdx) return;
  const slice = list.slice(fromIdx, toIdx);
  slice.forEach((vehicle, offset) => {
    const index = fromIdx + offset;
    const wrapper = document.createElement('div');
    wrapper.innerHTML = buildVehicleCardHtml(vehicle, language).trim();
    const card = wrapper.firstElementChild;
    if (!card) return;
    grid.appendChild(card);
    try {
      hydrateRentalVehicleCard(card, vehicle, language);
    } catch (err) {
      console.error('[TK168 rental] fleet card hydrate failed', vehicle?.id, err);
    }
    window.setTimeout(() => {
      card.classList.add('visible');
    }, index * 45);
  });
  if (typeof bindVehicleCardLikes === 'function') {
    bindVehicleCardLikes(grid);
  }
  window.TK168Renderers?.bindVehicleCardCoverSkeletons?.(grid);
}

function loadMoreRentalFleetChunk(language = getLanguage()) {
  const list = getRentableVehicles();
  const prev = rentalFleetVisibleLimit;
  if (list.length <= prev) return false;
  rentalFleetVisibleLimit = Math.min(list.length, prev + RENTAL_FLEET_BATCH);
  if (rentalFleetVisibleLimit <= prev) return false;
  appendRentableFleetCards(language, prev, rentalFleetVisibleLimit, list);
  updateRentalFleetLoadMoreUi(list);
  return true;
}

function buildInquiryHref(vehicle) {
  const id = String(vehicle?.id || '').trim();
  return `rental-inquiry.html?id=${encodeURIComponent(id)}`;
}

function buildVehicleCardHtml(vehicle, language) {
  const detailUrl = typeof buildDetailUrl === 'function'
    ? buildDetailUrl(vehicle.id, { from: 'rental' })
    : `detail.html?id=${encodeURIComponent(vehicle.id)}&from=rental`;

  const statusSlug = normalizeRentalFleetStatus(getVehicleRentalProfile(vehicle).rentalStatus);

  if (typeof buildInventoryCardHTML === 'function') {
    return `
      <article class="v-card rental-v-card" data-vehicle-id="${escapeHtml(vehicle.id)}" data-rental-status="${escapeHtml(statusSlug)}" data-detail-url="${escapeHtml(detailUrl)}">
        ${buildInventoryCardHTML(vehicle, detailUrl, { rentalFleet: true })}
      </article>
    `;
  }

  const profile = getVehicleRentalProfile(vehicle);
  const title = getVehicleName(vehicle, language);
  const type =
    getVehicleFieldLabel('bodyStyle', vehicle.bodyStyle, language) || '';
  const inquiryHref = buildInquiryHref(vehicle);

  return `
    <article class="rental-card" data-rental-status="${escapeHtml(statusSlug)}">
      <div class="rental-card-media">
        <img src="assets/images/${escapeHtml(vehicle.photo)}" alt="${escapeHtml(title)}">
      </div>
      <div class="rental-card-body">
        <div class="rental-card-head">
          <h3 class="rental-card-name">${escapeHtml(title)}</h3>
          <p class="rental-card-meta">${escapeHtml(`${vehicle.year} · ${type} · ${fleetCardStatusLabel(language, vehicle)}`)}</p>
        </div>
        <div class="rental-card-facts">
          <div class="rental-fact">
            <span class="rental-fact-label">${escapeHtml(copy('fleet.card.mileage', language))}</span>
            <span class="rental-fact-value">${escapeHtml(formatMileage(vehicle.mileage, language, vehicle.mileageUnit))}</span>
          </div>
          <div class="rental-fact">
            <span class="rental-fact-label">${escapeHtml(copy('fleet.card.dailyRate', language))}</span>
            <span class="rental-fact-value">${escapeHtml(formatRate(profile.dailyRate, language))}</span>
          </div>
        </div>
        <a class="rental-card-action" href="${escapeHtml(inquiryHref)}">${escapeHtml(copy('fleet.card.action', language))}</a>
      </div>
    </article>
  `;
}

function hydrateRentalVehicleCard(card, vehicle, language) {
  if (!card || !card.classList.contains('v-card')) return;
  const profile = getVehicleRentalProfile(vehicle);
  const statusSlug = normalizeRentalFleetStatus(profile.rentalStatus);
  card.dataset.rentalStatus = statusSlug;
  const bodyLine = getVehicleFieldLabel('bodyStyle', vehicle.bodyStyle, language);
  const fuel = getVehicleFieldLabel('fuel', vehicle.fuel, language);
  const inquiryHref = buildInquiryHref(vehicle);
  const detailUrl = typeof buildDetailUrl === 'function'
    ? buildDetailUrl(vehicle.id, { from: 'rental' })
    : `detail.html?id=${encodeURIComponent(vehicle.id)}&from=rental`;

  card.dataset.detailUrl = detailUrl;
  card.removeAttribute('onclick');
  if (card.dataset.rentalCardBound !== '1') {
    card.dataset.rentalCardBound = '1';
    card.addEventListener('click', (event) => {
      if (event.target.closest('a,button')) return;
      const targetUrl = card.dataset.detailUrl;
      if (!targetUrl) return;
      if (window.TK168LayoutShell?.navigate) {
        window.TK168LayoutShell.navigate(targetUrl);
      } else {
        window.location.href = targetUrl;
      }
    });
  }

  const meta = card.querySelector('.v-card-meta');
  if (meta) {
    meta.textContent = [vehicle.year, bodyLine, vehicle.grade, fleetCardStatusLabel(language, vehicle)].filter(Boolean).join(' · ');
  }

  const specSpans = Array.from(card.querySelectorAll('.v-spec span'));
  const powertrain =
    (window.TK168_DATA?.formatVehicleEngineAndForcedInductionLine?.(vehicle, language)
      || window.TK168_DATA?.formatVehicleEngineLine?.(vehicle)
      || '').trim() || '—';
  const specRows = [
    { label: copy('fleet.card.mileage', language), value: formatMileage(vehicle.mileage, language, vehicle.mileageUnit) },
    { label: copy('fleet.card.powertrain', language), value: powertrain },
    { label: copy('fleet.card.fuel', language), value: fuel },
    {
      label: copy('fleet.card.minDays', language),
      value: language === 'ja'
        ? `${formatMinDays(profile.minDays, language)}〜`
        : (language === 'en' ? `${formatMinDays(profile.minDays, language)} minimum` : `${formatMinDays(profile.minDays, language)}起`)
    }
  ];

  specSpans.forEach((span, index) => {
    const row = specRows[index];
    if (!row) return;
    span.textContent = row.value;
    span.title = `${row.label}: ${row.value}`;
  });

  const priceLabel = card.querySelector('.v-price-label');
  if (priceLabel) priceLabel.textContent = copy('fleet.card.dailyRate', language);

  const priceValue = card.querySelector('.v-price');
  if (priceValue) priceValue.textContent = formatRate(profile.dailyRate, language);

  const subLabel = card.querySelector('.v-price-sub-label');
  if (subLabel) subLabel.textContent = copy('fleet.card.deposit', language);

  const subValue = card.querySelector('.v-price-sub');
  if (subValue) subValue.textContent = getRentalManJpyDisplayPrice(profile.deposit) || '-';

  const vehicleId = String(vehicle?.id || '').trim();
  card.dataset.rentalVehicleId = vehicleId;

  const detailBtn = card.querySelector('.v-detail-btn');
  if (detailBtn) {
    detailBtn.textContent = copy('fleet.card.action', language);
    detailBtn.setAttribute('href', inquiryHref);
    detailBtn.dataset.rentalVehicleId = vehicleId;
    detailBtn.setAttribute('aria-label', `${copy('fleet.card.action', language)} ${getVehicleName(vehicle, language)}`);
  }

  card.querySelectorAll('a').forEach((anchor) => {
    if (anchor.dataset.rentalStopBound === '1') return;
    anchor.dataset.rentalStopBound = '1';
    anchor.addEventListener('click', (event) => {
      event.stopPropagation();
    });
  });
}

function renderRentableVehicles(language = getLanguage()) {
  const grid = document.getElementById('rentalVehicleGrid');
  const empty = document.getElementById('rentalFleetEmpty');
  if (!grid || !empty) return;
  const renderMode = isRentalFleetMobileView() ? 'mobile' : 'desktop';

  const rentableVehicles = getRentableVehicles();
  if (!rentableVehicles.length) {
    const dataNotHydrated = !Array.isArray(window.TK168_API_VEHICLES)
      && !Array.isArray(window.TK168_API_RENTALS);
    if (dataNotHydrated) {
      empty.hidden = true;
      const placeholder = renderMode === 'mobile' ? 4 : 8;
      window.TK168Renderers?.renderVehicleSkeletons?.(grid, placeholder);
      rentalFleetRenderMode = renderMode;
      rentalFleetVisibleLimit = 0;
      updateRentalFleetLoadMoreUi(rentableVehicles);
      return;
    }
    grid.innerHTML = '';
    empty.hidden = false;
    rentalFleetRenderMode = renderMode;
    rentalFleetVisibleLimit = 0;
    updateRentalFleetLoadMoreUi(rentableVehicles);
    return;
  }

  ensureRentalFleetVisibleLimit(rentableVehicles);
  const slice = rentableVehicles.slice(0, rentalFleetVisibleLimit);

  empty.hidden = true;
  if (renderMode !== 'mobile') {
    grid.scrollLeft = 0;
  }
  grid.innerHTML = slice.map((vehicle) => buildVehicleCardHtml(vehicle, language)).join('');

  if (typeof bindVehicleCardLikes === 'function') {
    bindVehicleCardLikes(grid);
  }
  window.TK168Renderers?.bindVehicleCardCoverSkeletons?.(grid);

  const cards = Array.from(grid.querySelectorAll('.v-card'));
  slice.forEach((vehicle, index) => {
    try {
      hydrateRentalVehicleCard(cards[index], vehicle, language);
    } catch (err) {
      console.error('[TK168 rental] fleet card hydrate failed', vehicle?.id, err);
    }
  });

  cards.forEach((card, index) => {
    window.setTimeout(() => {
      card.classList.add('visible');
    }, index * 45);
  });

  rentalFleetRenderMode = renderMode;
  updateRentalFleetLoadMoreUi(rentableVehicles);
}

function bindRentalFleetLoadMoreButton() {
  const btn = document.getElementById('rentalFleetLoadMore');
  if (!btn || btn.dataset.bound === '1') return;
  btn.dataset.bound = '1';
  btn.addEventListener('click', () => {
    loadMoreRentalFleetChunk(getLanguage());
  });
}

function bindRentalFleetViewportSync() {
  if (window.__rentalFleetViewportSyncBound === '1') return;
  window.__rentalFleetViewportSyncBound = '1';

  const scheduleSync = () => {
    if (rentalFleetResizeFrame) cancelAnimationFrame(rentalFleetResizeFrame);
    rentalFleetResizeFrame = requestAnimationFrame(() => {
      rentalFleetResizeFrame = 0;
      const nextMode = isRentalFleetMobileView() ? 'mobile' : 'desktop';
      if (nextMode !== rentalFleetRenderMode) {
        renderRentableVehicles(getLanguage());
      }
    });
  };

  if (typeof rentalFleetMobileViewport.addEventListener === 'function') {
    rentalFleetMobileViewport.addEventListener('change', scheduleSync);
  } else if (typeof rentalFleetMobileViewport.addListener === 'function') {
    rentalFleetMobileViewport.addListener(scheduleSync);
  }

  window.addEventListener('resize', scheduleSync);
}

function applyRentalCopy(language = getLanguage()) {
  document.title = copy('meta.title', language);

  document.querySelectorAll('[data-rental-key]').forEach((element) => {
    const key = element.dataset.rentalKey || '';
    const text = copy(key, language);
    if (text) element.textContent = text;
  });

  document.querySelectorAll('[data-rental-placeholder]').forEach((element) => {
    const key = element.dataset.rentalPlaceholder || '';
    const text = copy(key, language);
    if (text) element.setAttribute('placeholder', text);
  });
}

function bindRentalFlowSample() {
  const toggle = document.querySelector('[data-rental-sample-toggle]');
  const panel = document.querySelector('[data-rental-sample-panel]');
  const card = document.querySelector('[data-rental-flow-sample-card]');
  if (!toggle || !panel || toggle.dataset.bound === '1') return;

  toggle.dataset.bound = '1';
  toggle.addEventListener('click', () => {
    const shouldOpen = toggle.getAttribute('aria-expanded') !== 'true';
    toggle.setAttribute('aria-expanded', shouldOpen ? 'true' : 'false');
    panel.hidden = !shouldOpen;
    card?.classList.toggle('is-expanded', shouldOpen);
  });
}

function initRentalPage() {
  window.TK168CommonLinks?.applyCommonLinks();
  window.TK168PageChrome?.applyPageChrome({
    pageKey: 'rental',
    inventoryHref: 'brand.html',
    serviceHref: 'service.html',
    rentalHref: 'rental.html'
  });

  const language = getLanguage();
  applyRentalCopy(language);
  bindRentalFlowSample();
  bindRentalFlowContactsReveal();
  void hydrateRentalFlowContactsFromApi();
  bindRentalFleetLoadMoreButton();
  bindRentalFleetViewportSync();
  wireRentalInquiryIdStash();
  renderRentableVehicles(language);
}

window.addEventListener('tk168:languagechange', (event) => {
  const language = event.detail?.language || 'ja';
  applyRentalCopy(language);
  renderRentableVehicles(language);
});

document.addEventListener('tk168:data-updated', (event) => {
  if (event.detail?.vehicles) {
    window.TK168_DATA?.refreshVehiclesFromApiHydrate?.();
  }
  if (event.detail?.rentals || event.detail?.vehicles) {
    rentalFleetVisibleLimit = 0;
    renderRentableVehicles(getLanguage());
  }
});

initRentalPage();
