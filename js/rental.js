const {
  getDisplayPrice,
  getVehicleName,
  getVehicleTypeLabel,
  getVehicleFieldLabel,
  getVehicleRentalProfile,
  getRentableVehicles,
  buildDetailUrl
} = window.TK168_DATA;
const { buildInventoryCardHTML, bindVehicleCardLikes } = window.TK168Renderers || {};
const RENTAL_FLEET_PAGE_SIZE = 6;
let rentalFleetCurrentPage = 1;
const rentalFleetMobileViewport = window.matchMedia('(max-width: 760px)');
let rentalFleetRenderMode = rentalFleetMobileViewport.matches ? 'mobile' : 'desktop';
let rentalFleetScrollFrame = 0;
let rentalFleetResizeFrame = 0;

function getLanguage() {
  return window.TK168I18N?.getLanguage?.() || 'ja';
}

function copy(key, language = getLanguage()) {
  return RENTAL_COPY[language]?.[key] || RENTAL_COPY.ja[key] || RENTAL_COPY.zh[key] || key;
}

function escapeHtml(value) {
  return String(value ?? '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}

function formatMileage(value) {
  return `${String(value || '0')} km`;
}

function formatRate(value, language) {
  const display = getDisplayPrice(value, language) || '-';
  if (language === 'en') return `${display}/day`;
  return language === 'ja' ? `${display}/日` : `${display}/天`;
}

function formatMinDays(days, language) {
  if (language === 'en') return `${days} day${Number(days) === 1 ? '' : 's'}`;
  return language === 'ja' ? `${days}日` : `${days}天`;
}

function isRentalFleetMobileView() {
  return rentalFleetMobileViewport.matches;
}

function getRentalFleetLeadIndex() {
  return rentalFleetRenderMode === 'mobile'
    ? rentalFleetCurrentPage - 1
    : (rentalFleetCurrentPage - 1) * RENTAL_FLEET_PAGE_SIZE;
}

function getRentalFleetCards() {
  const grid = document.getElementById('rentalVehicleGrid');
  return Array.from(grid?.querySelectorAll('.v-card') || []);
}

function getClosestRentalFleetCardIndex() {
  const grid = document.getElementById('rentalVehicleGrid');
  const cards = getRentalFleetCards();
  if (!grid || !cards.length) return 0;

  const viewportCenter = grid.scrollLeft + (grid.clientWidth / 2);
  let nearestIndex = 0;
  let nearestDistance = Number.POSITIVE_INFINITY;

  cards.forEach((card, index) => {
    const cardCenter = card.offsetLeft + (card.offsetWidth / 2);
    const distance = Math.abs(cardCenter - viewportCenter);
    if (distance < nearestDistance) {
      nearestDistance = distance;
      nearestIndex = index;
    }
  });

  return nearestIndex;
}

function scrollToRentalFleetCard(index, behavior = 'smooth') {
  const cards = getRentalFleetCards();
  if (!cards.length) return;

  const clampedIndex = Math.max(0, Math.min(cards.length - 1, index));
  cards[clampedIndex].scrollIntoView({
    behavior,
    block: 'nearest',
    inline: 'start'
  });
}

function buildInquiryHref(vehicle) {
  const params = new URLSearchParams();
  params.set('id', vehicle.id);
  return `rental-inquiry.html?${params.toString()}`;
}

function buildVehicleCardHtml(vehicle, language) {
  const detailUrl = typeof buildDetailUrl === 'function'
    ? buildDetailUrl(vehicle.id)
    : `detail.html?id=${encodeURIComponent(vehicle.id)}`;

  if (typeof buildInventoryCardHTML === 'function') {
    return `
      <article class="v-card rental-v-card" data-vehicle-id="${escapeHtml(vehicle.id)}" data-detail-url="${escapeHtml(detailUrl)}">
        ${buildInventoryCardHTML(vehicle, detailUrl)}
      </article>
    `;
  }

  const profile = getVehicleRentalProfile(vehicle);
  const title = getVehicleName(vehicle, language);
  const type = getVehicleTypeLabel(vehicle.type, language);
  const inquiryHref = buildInquiryHref(vehicle);

  return `
    <article class="rental-card">
      <div class="rental-card-media">
        <img src="assets/images/${escapeHtml(vehicle.photo)}" alt="${escapeHtml(title)}">
      </div>
      <div class="rental-card-body">
        <div class="rental-card-head">
          <h3 class="rental-card-name">${escapeHtml(title)}</h3>
          <p class="rental-card-meta">${escapeHtml(`${vehicle.year} · ${type} · ${copy('fleet.card.available', language)}`)}</p>
        </div>
        <div class="rental-card-facts">
          <div class="rental-fact">
            <span class="rental-fact-label">${escapeHtml(copy('fleet.card.mileage', language))}</span>
            <span class="rental-fact-value">${escapeHtml(formatMileage(vehicle.mileage))}</span>
          </div>
          <div class="rental-fact">
            <span class="rental-fact-label">${escapeHtml(copy('fleet.card.dailyRate', language))}</span>
            <span class="rental-fact-value">${escapeHtml(formatRate(profile.dailyRate, language))}</span>
          </div>
        </div>
        <a class="rental-card-action" href="${escapeHtml(inquiryHref)}">${escapeHtml(copy('fleet.card.action', language))}</a>
      </div>
    </article>
  `;
}

function hydrateRentalVehicleCard(card, vehicle, language) {
  if (!card || !card.classList.contains('v-card')) return;
  const profile = getVehicleRentalProfile(vehicle);
  const type = getVehicleTypeLabel(vehicle.type, language);
  const fuel = getVehicleFieldLabel('fuel', vehicle.fuel, language);
  const inquiryHref = buildInquiryHref(vehicle);
  const detailUrl = typeof buildDetailUrl === 'function'
    ? buildDetailUrl(vehicle.id)
    : `detail.html?id=${encodeURIComponent(vehicle.id)}`;

  card.dataset.detailUrl = detailUrl;
  card.removeAttribute('onclick');
  if (card.dataset.rentalCardBound !== '1') {
    card.dataset.rentalCardBound = '1';
    card.addEventListener('click', (event) => {
      if (event.target.closest('a,button')) return;
      const targetUrl = card.dataset.detailUrl;
      if (targetUrl) window.location.href = targetUrl;
    });
  }

  const meta = card.querySelector('.v-card-meta');
  if (meta) meta.textContent = `${vehicle.year} · ${type} · ${copy('fleet.card.available', language)}`;

  const specSpans = Array.from(card.querySelectorAll('.v-spec span'));
  const specRows = [
    { label: copy('fleet.card.mileage', language), value: formatMileage(vehicle.mileage) },
    { label: copy('fleet.card.fuel', language), value: fuel },
    { label: copy('fleet.card.dailyRate', language), value: formatRate(profile.dailyRate, language) },
    {
      label: copy('fleet.card.minDays', language),
      value: language === 'ja'
        ? `${formatMinDays(profile.minDays, language)}〜`
        : (language === 'en' ? `${formatMinDays(profile.minDays, language)} minimum` : `${formatMinDays(profile.minDays, language)}起`)
    }
  ];

  specSpans.forEach((span, index) => {
    const row = specRows[index];
    if (!row) return;
    span.textContent = row.value;
    span.title = `${row.label}: ${row.value}`;
  });

  const priceLabel = card.querySelector('.v-price-label');
  if (priceLabel) priceLabel.textContent = copy('fleet.card.dailyRate', language);

  const priceValue = card.querySelector('.v-price');
  if (priceValue) priceValue.textContent = formatRate(profile.dailyRate, language);

  const subLabel = card.querySelector('.v-price-sub-label');
  if (subLabel) subLabel.textContent = copy('fleet.card.deposit', language);

  const subValue = card.querySelector('.v-price-sub');
  if (subValue) subValue.textContent = getDisplayPrice(profile.deposit, language) || '-';

  const detailBtn = card.querySelector('.v-detail-btn');
  if (detailBtn) {
    detailBtn.textContent = copy('fleet.card.action', language);
    detailBtn.setAttribute('href', inquiryHref);
    detailBtn.setAttribute('aria-label', `${copy('fleet.card.action', language)} ${getVehicleName(vehicle, language)}`);
  }

  card.querySelectorAll('a').forEach((anchor) => {
    if (anchor.dataset.rentalStopBound === '1') return;
    anchor.dataset.rentalStopBound = '1';
    anchor.addEventListener('click', (event) => {
      event.stopPropagation();
    });
  });
}

function updateFleetPagination(page, totalPages, language, hasVehicles) {
  const pagination = document.getElementById('rentalFleetPagination');
  const prevButton = document.getElementById('rentalFleetPrev');
  const nextButton = document.getElementById('rentalFleetNext');
  const dotsWrap = document.getElementById('rentalFleetDots');
  if (!pagination || !prevButton || !nextButton || !dotsWrap) return;

  const mobilePaged = isRentalFleetMobileView();
  const effectiveTotalPages = hasVehicles
    ? Math.max(1, mobilePaged ? getRentableVehicles().length : totalPages)
    : 1;
  const effectivePage = hasVehicles
    ? (mobilePaged ? rentalFleetCurrentPage : page)
    : 1;

  pagination.hidden = !hasVehicles || effectiveTotalPages <= 1;
  prevButton.disabled = effectiveTotalPages <= 1 || effectivePage <= 1;
  nextButton.disabled = effectiveTotalPages <= 1 || effectivePage >= effectiveTotalPages;
  prevButton.setAttribute('aria-label', copy('fleet.pagination.prev', language));
  nextButton.setAttribute('aria-label', copy('fleet.pagination.next', language));

  window.TK168Renderers?.renderPaginationDots?.(dotsWrap, {
    totalCount: effectiveTotalPages,
    activeIndex: effectivePage - 1,
    dataAttribute: 'data-rental-page-dot',
    ariaLabelBuilder: (dotIndex) => `Page ${dotIndex + 1}`,
    onClick: (pageIndex) => goToRentalFleetPage(pageIndex + 1, language)
  });
}

function wireFleetPagination() {
  const prevButton = document.getElementById('rentalFleetPrev');
  const nextButton = document.getElementById('rentalFleetNext');
  if (!prevButton || !nextButton) return;

  if (prevButton.dataset.bound !== '1') {
    prevButton.dataset.bound = '1';
    prevButton.addEventListener('click', () => {
      goToRentalFleetPage(rentalFleetCurrentPage - 1, getLanguage());
    });
  }

  if (nextButton.dataset.bound !== '1') {
    nextButton.dataset.bound = '1';
    nextButton.addEventListener('click', () => {
      goToRentalFleetPage(rentalFleetCurrentPage + 1, getLanguage());
    });
  }
}

function goToRentalFleetPage(page, language = getLanguage()) {
  const rentableVehicles = getRentableVehicles();
  const totalPages = Math.max(1, isRentalFleetMobileView() ? rentableVehicles.length : Math.ceil(rentableVehicles.length / RENTAL_FLEET_PAGE_SIZE));
  rentalFleetCurrentPage = Math.max(1, Math.min(totalPages, page));

  if (isRentalFleetMobileView()) {
    scrollToRentalFleetCard(rentalFleetCurrentPage - 1);
    updateFleetPagination(rentalFleetCurrentPage, totalPages, language, rentableVehicles.length > 0);
    return;
  }

  renderRentableVehicles(language);
}

function renderRentableVehicles(language = getLanguage()) {
  const grid = document.getElementById('rentalVehicleGrid');
  const empty = document.getElementById('rentalFleetEmpty');
  if (!grid || !empty) return;
  const renderMode = isRentalFleetMobileView() ? 'mobile' : 'desktop';
  const leadIndex = getRentalFleetLeadIndex();

  const rentableVehicles = getRentableVehicles();
  if (!rentableVehicles.length) {
    rentalFleetCurrentPage = 1;
    delete grid.dataset.mobilePaged;
    grid.innerHTML = '';
    empty.hidden = false;
    rentalFleetRenderMode = renderMode;
    updateFleetPagination(1, 1, language, false);
    return;
  }

  const totalPages = Math.max(1, Math.ceil(rentableVehicles.length / RENTAL_FLEET_PAGE_SIZE));
  rentalFleetCurrentPage = renderMode === 'mobile'
    ? Math.max(1, Math.min(rentableVehicles.length, leadIndex + 1))
    : Math.max(1, Math.min(totalPages, Math.floor(leadIndex / RENTAL_FLEET_PAGE_SIZE) + 1));

  const pagedVehicles = renderMode === 'mobile'
    ? rentableVehicles
    : rentableVehicles.slice((rentalFleetCurrentPage - 1) * RENTAL_FLEET_PAGE_SIZE, ((rentalFleetCurrentPage - 1) * RENTAL_FLEET_PAGE_SIZE) + RENTAL_FLEET_PAGE_SIZE);

  empty.hidden = true;
  if (renderMode === 'mobile') {
    grid.dataset.mobilePaged = 'true';
  } else {
    delete grid.dataset.mobilePaged;
    grid.scrollLeft = 0;
  }
  grid.innerHTML = pagedVehicles.map((vehicle) => buildVehicleCardHtml(vehicle, language)).join('');

  if (typeof bindVehicleCardLikes === 'function') {
    bindVehicleCardLikes(grid);
  }

  const cards = Array.from(grid.querySelectorAll('.v-card'));
  pagedVehicles.forEach((vehicle, index) => {
    hydrateRentalVehicleCard(cards[index], vehicle, language);
  });

  cards.forEach((card, index) => {
    window.setTimeout(() => {
      card.classList.add('visible');
    }, index * 45);
  });

  rentalFleetRenderMode = renderMode;
  updateFleetPagination(rentalFleetCurrentPage, totalPages, language, true);

  if (renderMode === 'mobile') {
    requestAnimationFrame(() => {
      scrollToRentalFleetCard(rentalFleetCurrentPage - 1, 'auto');
    });
  }
}

function bindRentalFleetMobilePager() {
  const grid = document.getElementById('rentalVehicleGrid');
  if (!grid || grid.dataset.mobilePagerBound === '1') return;
  grid.dataset.mobilePagerBound = '1';

  grid.addEventListener('scroll', () => {
    if (!isRentalFleetMobileView()) return;
    if (rentalFleetScrollFrame) return;
    rentalFleetScrollFrame = requestAnimationFrame(() => {
      rentalFleetScrollFrame = 0;
      const nextPage = getClosestRentalFleetCardIndex() + 1;
      if (nextPage === rentalFleetCurrentPage) return;
      rentalFleetCurrentPage = nextPage;
      updateFleetPagination(rentalFleetCurrentPage, getRentableVehicles().length, getLanguage(), true);
    });
  }, { passive: true });

  const scheduleSync = () => {
    if (rentalFleetResizeFrame) cancelAnimationFrame(rentalFleetResizeFrame);
    rentalFleetResizeFrame = requestAnimationFrame(() => {
      rentalFleetResizeFrame = 0;
      const nextMode = isRentalFleetMobileView() ? 'mobile' : 'desktop';
      if (nextMode !== rentalFleetRenderMode) {
        renderRentableVehicles(getLanguage());
        return;
      }
      if (nextMode === 'mobile') {
        rentalFleetCurrentPage = getClosestRentalFleetCardIndex() + 1;
        updateFleetPagination(rentalFleetCurrentPage, getRentableVehicles().length, getLanguage(), true);
      }
    });
  };

  if (typeof rentalFleetMobileViewport.addEventListener === 'function') {
    rentalFleetMobileViewport.addEventListener('change', scheduleSync);
  } else if (typeof rentalFleetMobileViewport.addListener === 'function') {
    rentalFleetMobileViewport.addListener(scheduleSync);
  }

  window.addEventListener('resize', scheduleSync);
}

function applyRentalCopy(language = getLanguage()) {
  document.title = copy('meta.title', language);

  document.querySelectorAll('[data-rental-key]').forEach((element) => {
    const key = element.dataset.rentalKey || '';
    const text = copy(key, language);
    if (text) element.textContent = text;
  });

  document.querySelectorAll('[data-rental-placeholder]').forEach((element) => {
    const key = element.dataset.rentalPlaceholder || '';
    const text = copy(key, language);
    if (text) element.setAttribute('placeholder', text);
  });
}

function initRentalPage() {
  window.TK168CommonLinks?.applyCommonLinks();
  window.TK168PageChrome?.applyPageChrome({
    pageKey: 'rental',
    inventoryHref: 'brand.html',
    serviceHref: 'service.html',
    rentalHref: 'rental.html'
  });

  const language = getLanguage();
  applyRentalCopy(language);
  wireFleetPagination();
  bindRentalFleetMobilePager();
  renderRentableVehicles(language);
}

window.addEventListener('tk168:languagechange', (event) => {
  const language = event.detail?.language || 'ja';
  applyRentalCopy(language);
  renderRentableVehicles(language);
});

initRentalPage();
