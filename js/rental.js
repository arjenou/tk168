function resolveRentalApiBase() {
  if (typeof window.TK168_API_BASE === 'string') {
    return window.TK168_API_BASE.trim().replace(/\/+$/, '');
  }
  try {
    const host = String(location.hostname || '').toLowerCase();
    if (host === 'tk168.co.jp' || host === 'www.tk168.co.jp') {
      return '';
    }
  } catch {
    /* ignore */
  }
  return 'https://api.tk168.co.jp';
}

async function hydrateRentalFlowContactsFromApi() {
  const base = resolveRentalApiBase();
  let data = { phone: '', email: '', wechat: '', whatsapp: '' };
  try {
    const res = await fetch(`${base}/api/rental-contacts`, { credentials: 'omit', cache: 'no-store' });
    if (res.ok) {
      const j = await res.json();
      if (j && typeof j === 'object') data = { ...data, ...j };
    }
  } catch {
    /* ignore */
  }

  document.querySelectorAll('[data-rental-flow-contact]').forEach((el) => {
    const key = el.dataset.rentalFlowContact;
    if (!key || !['phone', 'email', 'wechat', 'whatsapp'].includes(key)) return;
    const raw = String(data[key] ?? '').trim();
    el.dataset.rentalContactValue = raw;
    if (raw) el.setAttribute('title', raw);
    else el.removeAttribute('title');

    if (el.tagName === 'A') {
      if (key === 'phone' && raw) {
        el.setAttribute('href', `tel:${raw.replace(/\s+/g, '')}`);
      } else if (key === 'email' && raw) {
        el.setAttribute('href', `mailto:${raw}`);
      } else if (key === 'whatsapp' && raw) {
        const digits = raw.replace(/\D/g, '');
        if (digits) el.setAttribute('href', `https://wa.me/${digits}`);
        else el.setAttribute('href', '#');
      } else {
        el.setAttribute('href', '#');
      }
    }
  });
}

let rentalContactModalEscapeHandler = null;

function closeRentalContactModal() {
  if (rentalContactModalEscapeHandler) {
    document.removeEventListener('keydown', rentalContactModalEscapeHandler);
    rentalContactModalEscapeHandler = null;
  }
  const root = document.getElementById('rentalContactModal');
  if (root) {
    root.hidden = true;
    root.setAttribute('aria-hidden', 'true');
  }
  document.body.classList.remove('rental-contact-modal-open');
}

async function rentalContactModalCopyClick() {
  const inp = document.querySelector('#rentalContactModalContent .rental-contact-modal-input');
  const t = String(inp?.value || '');
  if (!t) return;
  try {
    if (navigator.clipboard?.writeText) await navigator.clipboard.writeText(t);
    else throw new Error('no clipboard');
  } catch {
    try {
      inp.select();
      document.execCommand('copy');
    } catch {
      /* ignore */
    }
  }
}

function bindRentalContactModalChromeOnce() {
  const root = document.getElementById('rentalContactModal');
  if (!root || root.dataset.chromeBound === '1') return;
  root.dataset.chromeBound = '1';
  root.addEventListener('click', (e) => {
    if (e.target.closest('[data-rental-contact-modal-dismiss]')) closeRentalContactModal();
  });
  document.getElementById('rentalContactModalOk')?.addEventListener('click', closeRentalContactModal);
  document.getElementById('rentalContactModalCopy')?.addEventListener('click', rentalContactModalCopyClick);
}

function openRentalContactModal({ title, message, value }) {
  bindRentalContactModalChromeOnce();
  const root = document.getElementById('rentalContactModal');
  const titleEl = document.getElementById('rentalContactModalTitle');
  const content = document.getElementById('rentalContactModalContent');
  const okBtn = document.getElementById('rentalContactModalOk');
  const copyBtn = document.getElementById('rentalContactModalCopy');
  const lang = getLanguage();
  if (!root || !titleEl || !content || !okBtn || !copyBtn) return;

  titleEl.textContent = title;
  okBtn.textContent = copy('flow.contactModalOk', lang);
  copyBtn.textContent = copy('flow.contactModalCopy', lang);

  const hasValue = Boolean(String(value || '').trim());
  copyBtn.hidden = !hasValue;
  content.textContent = '';
  content.innerHTML = '';

  if (hasValue) {
    const inp = document.createElement('input');
    inp.type = 'text';
    inp.className = 'rental-contact-modal-input';
    inp.readOnly = true;
    inp.value = String(value).trim();
    content.appendChild(inp);
  } else {
    const p = document.createElement('p');
    p.className = 'rental-contact-modal-message';
    p.textContent = message || '';
    content.appendChild(p);
  }

  root.hidden = false;
  root.setAttribute('aria-hidden', 'false');
  document.body.classList.add('rental-contact-modal-open');

  rentalContactModalEscapeHandler = (e) => {
    if (e.key === 'Escape') closeRentalContactModal();
  };
  document.addEventListener('keydown', rentalContactModalEscapeHandler);

  if (hasValue) {
    const inp = content.querySelector('.rental-contact-modal-input');
    requestAnimationFrame(() => {
      inp?.focus();
      inp?.select();
    });
  } else {
    okBtn.focus();
  }
}

function bindRentalFlowContactsReveal() {
  const root = document.querySelector('.rental-flow-contact-actions');
  if (!root || root.dataset.rentalFlowContactsBound === '1') return;
  root.dataset.rentalFlowContactsBound = '1';

  root.addEventListener('click', (e) => {
    const el = e.target.closest('[data-rental-flow-contact]');
    if (!el) return;
    e.preventDefault();
    const key = el.dataset.rentalFlowContact;
    const raw = String(el.dataset.rentalContactValue || '').trim();
    const lang = getLanguage();
    const label = copy(`flow.contact.${key}`, lang);
    if (!raw) {
      openRentalContactModal({
        title: label,
        message: copy('flow.contactEmpty', lang),
        value: ''
      });
      return;
    }
    openRentalContactModal({ title: label, message: '', value: raw });
  });
}

